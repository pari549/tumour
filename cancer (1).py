# -------- Spike Encoding --------
def rate_coding(X, time_steps=20):
    X_norm = (X - X.min()) / (X.max() - X.min() + 1e-9)
    spikes = np.random.rand(X.shape[0], X.shape[1], time_steps) < X_norm[:, :, None]
    return spikes.astype(int)

TIME_STEPS = 20

X_train_spikes = rate_coding(X_train_df_pca, TIME_STEPS)
X_test_spikes = rate_coding(X_test_df_pca, TIME_STEPS)

# -------- Simple SNN --------
class SimpleSNN:
    def __init__(self, n_features, threshold=10):
        self.weights = np.random.randn(n_features)
        self.threshold = threshold

    def train(self, X_spikes, y):
        for i in range(X_spikes.shape[0]):
            spike_sum = X_spikes[i].sum(axis=1)
            if y[i] == 1:
                self.weights += spike_sum
            else:
                self.weights -= spike_sum

    def predict(self, X_spikes):
        preds = []
        for i in range(X_spikes.shape[0]):
            spike_sum = X_spikes[i].sum(axis=1)
            membrane = np.dot(self.weights, spike_sum)
            preds.append(1 if membrane > self.threshold else 0)
        return np.array(preds)

# -------- Train & Evaluate --------
snn = SimpleSNN(n_features=X_train_df_pca.shape[1])
snn.train(X_train_spikes, y_train)

y_pred_snn = snn.predict(X_test_spikes)

