import numpy as np
from sklearn.svm import LinearSVC
from sklearn.model_selection import train_test_split
from sklearn.metrics import accuracy_score, classification_report, confusion_matrix

# -------- Dragonfly Parameters --------
NUM_DRAGONFLIES = 8
MAX_ITER = 8
FEATURE_COUNT = X_train_pca.shape[1]

def fitness(mask, X, y):
    if np.sum(mask) == 0:
        return 0

    X_sel = X[:, mask == 1]

    X_tr, X_val, y_tr, y_val = train_test_split(
        X_sel, y, test_size=0.2, stratify=y, random_state=42
    )

    clf = LinearSVC(C=1.0, max_iter=3000)
    clf.fit(X_tr, y_tr)
    y_pred = clf.predict(X_val)

    return accuracy_score(y_val, y_pred)

# -------- Initialize Dragonflies --------
dragonflies = np.random.randint(0, 2, size=(NUM_DRAGONFLIES, FEATURE_COUNT))
fitness_scores = np.zeros(NUM_DRAGONFLIES)

best_mask = None
best_score = 0

print("Starting Dragonfly Optimization...")

for itr in range(MAX_ITER):
    print(f"Iteration {itr+1}/{MAX_ITER}")

    for i in range(NUM_DRAGONFLIES):
        score = fitness(dragonflies[i], X_train_pca, y_train)
        fitness_scores[i] = score

        if score > best_score:
            best_score = score
            best_mask = dragonflies[i].copy()

    for i in range(NUM_DRAGONFLIES):
        flip = np.random.rand(FEATURE_COUNT)
        dragonflies[i] = np.where(flip < 0.05, 1 - dragonflies[i], dragonflies[i])

print("Dragonfly completed.")
print("Selected features:", np.sum(best_mask))

# -------- Apply Selected Features --------
X_train_df_pca = X_train_pca[:, best_mask == 1]
X_test_df_pca = X_test_pca[:, best_mask == 1]

# -------- Train SVM --------
svm_m3 = LinearSVC(C=1.0, max_iter=5000)
